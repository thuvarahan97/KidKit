package com.example.hackxapp;

public class PointValue {
    String xValue;
    String yValue;

    public PointValue() {
    }

    public PointValue(String xValue, String yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public String getxValue() {
        return xValue;
    }

    public String getyValue() {
        return yValue;
    }
}
